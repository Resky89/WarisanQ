                            <h5>Contoh Kasus:</h5>
                            <p>
                                Seorang pria wafat, meninggalkan istri, lima anak perempuan, ayah dan ibu. Berdasarkan QS. An Nisa' (4) ayat 11-12, didapat pembagian sebagai berikut:
                            </p>
