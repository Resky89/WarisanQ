            <div class="accordion-body">
                <div class="prose max-w-none">
                    <p><strong>A. Definisi al-Hujub</strong></p>
                    <p>Al-hujub dalam bahasa Arab bermakna 'penghalang' atau 'penggugur'. Dalam Al-Qur'an Allah SWT berfirman:</p>
                    <blockquote>
                        "Sekali-kali tidak sesungguhnya mereka pada hari itu benar-benar terhalang dari (melihat) Tuhan mereka" (al-Muthaffifin: 15)
                    </blockquote>
                    <p>Yang dimaksud oleh ayat ini adalah kaum kuffar yang tidak dapat melihat Tuhan mereka di hari kiamat nanti.</p>
                    <p>Al-hujub juga bermakna 'tukang atau penjaga pintu', seseorang yang menghalangi orang untuk masuk ke tempat tertentu.</p>
                    <p>Dalam konteks warisan, al-hujub adalah menggugurkan hak ahli waris untuk menerima warisan, baik seluruhnya maupun sebagian, karena ada yang lebih berhak.</p>

                    <p><strong>B. Macam-macam al-Hujub</strong></p>
                    <p>Al-hujub terbagi menjadi dua:</p>
                    <ul>
                        <li><strong>Al-hujub bil washfi</strong> (penghalangan karena sifat): Seseorang yang terhalang dari hak waris, misalnya karena membunuh pewaris atau murtad.</li>
                        <li><strong>Al-hujub bi asy-syakhshi</strong> (penghalangan karena orang lain): Hak waris seseorang gugur karena ada yang lebih berhak.</li>
                    </ul>

                    <p><strong>Jenis-jenis al-Hujub bi asy-syakhshi:</strong></p>
                    <ol>
                        <li><strong>Hujub hirman:</strong> Menggugurkan seluruh hak waris seseorang, misalnya kakek terhalang oleh ayah, atau saudara terhalang oleh anak.</li>
                        <li><strong>Hujub nuqshan:</strong> Mengurangi hak waris seseorang, misalnya ibu yang seharusnya mendapat sepertiga menjadi seperenam karena pewaris memiliki anak.</li>
                    </ol>

                    <p><strong>Ahli Waris yang Tidak Terkena Hujub Hirman</strong></p>
                    <p>Enam orang ahli waris yang tidak mungkin terkena hujub hirman: anak kandung laki-laki, anak kandung perempuan, ayah, ibu, suami, dan istri.</p>

                    <p><strong>Ahli Waris yang Dapat Terkena Hujub Hirman</strong></p>
                    <p>Sebelas ahli waris laki-laki dan lima ahli waris perempuan dapat terkena hujub hirman. Beberapa contohnya adalah:</p>
                    <ul>
                        <li class="text-gray-800">Kakek terhalang oleh ayah.</li>
                        <li class="text-gray-800">Saudara kandung terhalang oleh ayah atau anak.</li>
                        <li class="text-gray-800">Cucu laki-laki terhalang oleh anak laki-laki.</li>
                    </ul>

                    <p><strong>C. Tentang Kasus Kolektif</strong></p>
                    <p>Kasus kolektif atau "musytarakah" adalah ketika saudara kandung dan saudara seibu mendapat hak waris yang sama, walaupun kaidah aslinya berbeda. Pendapat ulama terbagi menjadi dua mengenai kasus ini, satu pihak mengikuti kaidah asli sementara yang lain mengkolektifkan hak waris.</p>

                    <p><strong>Perbedaan Pendapat Para Fuqaha</strong></p>
                    <p>Dalam kasus musytarakah, ada perbedaan pendapat di kalangan fuqaha: Mazhab Hanafi dan Hambali menggugurkan hak saudara kandung, sedangkan Mazhab Maliki dan Syafi'i mengkolektifkan hak mereka.</p>

                    <p><strong>Kesimpulan</strong></p>
                    <p>Dalam faraid, al-hujub adalah konsep penting yang menentukan siapa yang berhak menerima warisan dan siapa yang terhalang. Kasus-kasus musytarakah menunjukkan fleksibilitas hukum Islam dalam menghadapi situasi yang kompleks.</p>
