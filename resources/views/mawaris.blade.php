                    </p>
                    <ol class="list-decimal list-inside space-y-2 mb-6">
                        <li class="text-gray-800">Anak lelaki</li>
                        <li class="text-gray-800">Cucu lelaki dari anak lelaki, dan seterusnya dari keturunannya yang lelaki</li>
                        <li class="text-gray-800">Bapak</li>
                        <li class="text-gray-800">Kakek (dari pihak bapak) dan ke atasnya dari jalur lelaki</li>
                        <li class="text-gray-800">Suami</li>
                        <li class="text-gray-800">Saudara lelaki sekandung</li>
                        <li class="text-gray-800">Saudara lelaki sebapak</li>
                        <li class="text-gray-800">Saudara lelaki seibu</li>
                        <li class="text-gray-800">Anak lelaki dari saudara lelaki sekandung (keponakan), dan seterusnya dari keturunannya yang lelaki</li>
                        <li class="text-gray-800">Anak lelaki dari saudara lelaki sebapak (keponakan), dan seterusnya dari keturunannya yang lelaki</li>
                        <li class="text-gray-800">Paman (saudara bapak sekandung)</li>
                        <li class="text-gray-800">Paman (saudara bapak sebapak)</li>
                        <li class="text-gray-800">Anak lelaki dari paman/saudara bapak sekandung (sepupu), dan seterusnya dari keturunannya yang lelaki</li>
                        <li class="text-gray-800">Anak lelaki dari paman/saudara bapak sebapak (sepupu), dan seterusnya dari keturunannya yang lelaki</li>
                        <li class="text-gray-800">Seorang lelaki yang membebaskan budak (mu'tiq), dan ashabah-nya dari jenis 'ashabah bin-nafsi.</li>
                    </ol>

