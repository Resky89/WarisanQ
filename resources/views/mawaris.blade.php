                <div class="prose max-w-none">
                    <p class="text-gray-800 leading-relaxed mb-4">
                        Dalam ilmu fara'idh, terdapat istilah <strong class="font-semibold">At-Tarikah</strong>. Menurut bahasa, artinya barang peninggalan mayit. Adapun menurut istilah, ulama berbeda pendapat. Sedangkan menurut jumhur ulama ialah, semua harta atau hak secara umum yang menjadi milik si mayit. Lihat Fiqhul Islam Wa Adillatih 8/270.
                    </p>
                    <p>
                        Muhammad bin Abdullah At-Takruni berkata: "At-Tarikah ialah, segala sesuatu yang ditinggalkan oleh mayit, berupa harta yang ia peroleh selama hidupnya di dunia, atau hak dia yang ada pada orang lain, seperti barang yang dihutang, atau gajinya, atau yang akan diwasiatkan, atau amanatnya, atau barang yang digadaikan, atau barang baru yang diperoleh sebab terbunuhnya dia, atau kecelakaan berupa santunan ganti rugi." Lihat kitab Al-Mualim Fil Fara'idh hal.119.
                    </p>
                    <p>
                        Adapun barang yang tidak berhak diwaris, diantaranya:
                    </p>
                    <ol>
                        <li>
                            Peralatan tidur untuk isteri dan peralatan yang khusus bagi dirinya, atau pemberian suami kepada isterinya semasa hidupnya. Lihat Fatawa Lajnah Daimah Lil Buhuts Al-Ilmiah wal Ifta 16/429.
                        </li>
                        <li>
                            Harta yang telah diwakafkan oleh mayit, seperti kitab dan lainnya. Lihat Fatawa Lajnah Daimah Lil Buhuts Al-Ilmiah wal Ifta 16/466.
                        </li>
                        <li>
                            Barang yang diperoleh dengan cara haram, seperti barang curian, hendaknya dikembalikan kepada pemiliknya, atau diserahkan kepada yang berwajib. Lihat keterangannya di dalam kitab Al-Muntaqa Min Fatawa, Dr Shalih Fauzan 5/238.
                        </li>
                    </ol>
                    <p>
                        Semua barang peninggalan mayit bukan berarti mutlak menjadi milik ahli waris, karena ada hak lainnya yang harus diselesaikan sebelum harta peninggalan tersebut dibagi. Hak-hak yang harus diselesaikan sebelum harta waris tersebut dibagi ialah sebagai berikut:
                    </p>
                    <ol>
                        <li>
                            <strong>Mu'nat Tajhiz Atau Perawatan Jenazah:</strong> Kebutuhan perawatan jenazah hingga penguburannya. Misalnya meliputi pembelian kain kafan, upah penggalian tanah, upah memandikan, bahkan perawatan selama dia sakit. Semua biaya ini diambilkan dari harta si mayit sebelum dilakukan hal lainnya. Berdasarkan perkataan Ibnu Abbas Radhiyallahu 'anhu, Rasulullah Shallallahu 'alaihi wa sallam bersabda: وَكَفْنُوهُ فِي تَوْبَيْهِ (Dan kafanillah dia dengan dua pakaianya). [Hadits Riwayat Bukhari 2/656, Muslim 2/866]
                        </li>
                        <li>
                            <strong>Al-Huquq Al-Muta'aliqah Bi Ainit Tarikah:</strong> Hak-hak yang berhubungan dengan harta waris. Misalnya barang yang digadaikan oleh mayit, hendaknya diselesaikan dengan menggunakan harta si mayit sebelum hartanya diwaris. Bahkan menurut Imam Syafi'i, Hanafi, dan Malik, didahulukan hak ini sebelum kebutuhan perawatan jenazah, karena berhubungan dengan harta si mayit. Lihat Fiqhul Islami wa Adillatihi 8/274. Dalilnya ialah, karena perkara ini termasuk hutang yang harus diselesaikan oleh si mayit sebagaimana disebutkan dalam surat An-Nisa ayat 12, yaitu: "Sesudah dibayar hutangnya."
                        </li>
                        <li>
                            <strong>Ad-Duyun Ghairu Al-Muta'aliqah Bit Tarikah:</strong> Hutang si mayit. Apabila si mayit mempunyai hutang, baik yang berhubungan dengan berhutang kepada Allah Subhanahu wa Ta'ala, seperti membayar zakat dan kafarah, atau yang berhubungan dengan anak Adam, seperti berhutang kepada orang lain, pembayaran gaji pegawainya, barang yang dibeli belum dibayar, melunasi pembayaran, maka sebelum diwaris, harta si mayit diambil untuk melunasinya. Dalilnya ialah: مِنْ بَعْدِ وَصِيَّةٍ يُوصِينَ بِهَا أَوْ دَيْنٍ "Sesudah dipenuhi wasiat yang mereka buat atau (dan) sesudah dibayar hutangnya dengan tidak memberi madharat (kepada ahli waris)." [An-Nisa: 12]
                        </li>
                        <li>
                            <strong>Tanfidzul Wasiyyah:</strong> Menunaikan wasiat. Sebelum harta diwaris, hendaknya diambil untuk menunaikan wasiat si mayit, bila wasiat itu bukan untuk ahli waris, karena ada larangan hal ini, dan bukan wasiat yang mengandung unsur maksiat, karena ada larangan mentaati perintah maksiat. Wasiat ini tidak boleh melebihi sepertiga, karena merupakan larangan. Dalilnya, lihat surat An-Nisa ayat 12 yaitu: "Sesudah dipenuhi wasiat yang mereka buat."
                        </li>
                    </ol>
                    <p>
                        Jika empat perkara di atas telah ditunaikan, dan ternyata masih ada sisa hak milik si mayit, maka itu dinamakan <strong>Tarikah</strong> atau bagian bagi ahli waris yang masih hidup. Dan saat pembagian harta waris, jika ada anggota keluarga lainnya yang tidak mendapatkan harta waris ikut hadir, sebaiknya diberi sekedarnya, agar dia ikut merasa senang, sebagaimana firman Allah dalam surat An-Nisa ayat 8.
                    </p>
