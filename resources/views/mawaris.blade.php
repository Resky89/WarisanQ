        </h2>
        <div id="flush-collapseFive" class="accordion-collapse collapse" data-bs-parent="#accordionKhi">
            <div class="accordion-body">
                <div class="prose max-w-none">
                    <div class="space-y-6">
                        <div>
                            <h3 class="text-xl font-bold mb-4">Ar-radd</h3>
                            <p class="text-gray-800 leading-relaxed">Ar-radd dalam bahasa Arab berarti 'kembali/kembalikan' atau 'berpaling/palingkan'. Contoh dalam Al-Qur'an:</p>
                            <blockquote class="pl-4 border-l-4 border-gray-300 italic my-4">
                                <p>"Musa berkata: 'Itulah (tempat) yang kita cari. Lalu keduanya kembali, mengikuti jejak mereka semula." (al-Kahfi: 64)</p>
                                <p>"Dan Allah menghalau orang-orang yang kafir itu yang keadaan mereka penuh kejengkelan ..." (al-Ahzab: 25)</p>
                            </blockquote>

                            <p class="text-gray-800 leading-relaxed">Dalam doa: "Allahumma radda kaidahum 'annii" (Ya Allah, palingkanlah/halaulah tipu daya mereka terhadapku).</p>

                            <p class="text-gray-800 leading-relaxed">Ar-radd menurut ulama faraid: berkurangnya pokok masalah dan bertambahnya jumlah bagian ashhabul furudh. Ar-radd kebalikan dari al-'aul.</p>
                        </div>

                        <div>
                            <h4 class="text-lg font-semibold mb-3">Syarat-syarat Ar-Radd</h4>
                            <ul class="list-disc pl-5 space-y-2">
                                <li>Adanya ashhabul furudh</li>
                                <li>Tidak adanya 'ashabah</li>
                                <li>Ada sisa harta waris</li>
                            </ul>
                        </div>

                        <div>
                            <h5>Ahli Waris yang Berhak Mendapat Ar-Radd</h5>
                            <p>Ar-radd dapat melibatkan semua ashhabul furudh, kecuali suami dan istri. Ahli waris yang bisa menerima ar-radd:</p>
                            <ul>
                                <li>Anak perempuan</li>
                                <li>Cucu perempuan keturunan anak laki-laki</li>
                                <li>Saudara kandung perempuan</li>
                                <li>Saudara perempuan seayah</li>
                                <li>Ibu kandung</li>
                                <li>Nenek sahih (ibu dari bapak)</li>
                                <li>Saudara perempuan seibu</li>
                                <li>Saudara laki-laki seibu</li>
                            </ul>
                            <h5>Ahli Waris yang Tidak Mendapat Ar-Radd</h5>
                            <p>Ahli waris dari ashhabul furudh yang tidak bisa menerima ar-radd hanyalah suami dan istri, karena kekerabatan ini terputus karena kematian.</p>
                            <h5>Macam-macam Ar-Radd</h5>
                            <ol>
                                <li>Adanya ahli waris pemilik bagian yang sama, tanpa suami atau istri.</li>
                                <li>Adanya pemilik bagian yang berbeda, tanpa suami atau istri.</li>
                                <li>Adanya pemilik bagian yang sama, dengan suami atau istri.</li>
                                <li>Adanya pemilik bagian yang berbeda, dengan suami atau istri.</li>
                            </ol>
                            <h5>Hukum Keadaan Pertama</h5>
                            <p>Jika ahli waris hanya terdiri dari sahib fardh dengan bagian yang sama dan tidak ada suami atau istri, pembagian berdasarkan jumlah ahli waris.</p>
                            <h5>Hukum Keadaan Kedua</h5>
                            <p>Jika ada bagian ahli waris yang beragam dan tidak ada suami atau istri, pembagian dihitung dari nilai bagiannya, bukan per kepala.</p>
                            <h5>Hukum Keadaan Ketiga</h5>
                            <p>Jika semua ahli waris dari sahib fardh yang sama dengan suami atau istri, pokok masalah diambil dari sahib fardh yang tidak dapat di-radd-kan.</p>
                            <h5>Hukum Keadaan Keempat</h5>
                            <p>Jika terdapat ashhabul furudh yang beragam bagiannya dengan suami atau istri, masalah dibuat dalam dua ilustrasi dengan diagram untuk solusi.</p>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    </div>

